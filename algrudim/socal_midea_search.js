const watch = ["science", "fiction","war"];

function ShowPost(post, watch) {
    let result = [];
    let stor = post.filter(Posts => Posts.category === watch[0]);
    
    // run a loop    
    for (let i = 1; i < watch.length; i++) {
        const category = watch[i];
        const simelerPost = post.filter(Posts => Posts.category === category);
        
        stor = [...stor, ...simelerPost];
        result = stor;
    }

    return result.sort(() => 0.5 - Math.random());
}
